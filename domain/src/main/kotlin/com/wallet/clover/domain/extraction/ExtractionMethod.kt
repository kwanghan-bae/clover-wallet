package com.wallet.clover.domain.extraction

/**
 * 로또 번호 추출 방법을 정의하는 열거형 클래스입니다.
 * 각 추출 방법은 기획서에 명시된 미신적 방법론에 기반합니다.
 */
enum class ExtractionMethod {
    /**
     * 꿈 해몽을 기반으로 번호를 추출합니다.
     * 특정 꿈 키워드와 관련된 숫자를 포함하여 번호를 생성합니다.
     */
    DREAM,

    /**
     * 사주팔자를 기반으로 번호를 추출합니다.
     * 사용자의 생년월일을 활용하여 행운의 숫자를 생성합니다.
     */
    SAJU,

    /**
     * 통계적 패턴을 기반으로 번호를 추출합니다.
     * 과거 당첨 번호 중 자주 나온 번호(Hot)와 거의 나오지 않은 번호(Cold)를 활용합니다.
     */
    STATISTICS_HOT,
    STATISTICS_COLD,

    /**
     * 별자리 운세를 기반으로 번호를 추출합니다.
     * 사용자의 별자리에 해당하는 행운의 숫자를 포함하여 번호를 생성합니다.
     */
    HOROSCOPE,
}
